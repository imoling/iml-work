-- 本体动作的岗位授权：谁有权执行这个动作。
--
-- 为什么必须有：此前本体动作**没有任何权限概念**——只要岗位的业务域侧重命中（如 PLANT），
-- 一线「装置操作工」的分身就能执行 ProductionOrder.approve，**批准生产指令**。
-- 技能有 allowed_roles，本体动作却是裸奔的。在危化行业这是安全事故级的漏洞。
--
-- 授权单位是**岗位**（不是平台角色）：员工是以某个岗位分身的身份在干活，
-- 「批准生产指令」天然属于「生产运行部领导」的职权，操作工岗位就不该拥有这个动作。
--
-- 语义：空数组/NULL = 不限岗位（向后兼容既有本体动作，不因升级而全部失效）；
--       非空 = 只有列出的岗位可执行，其它岗位明确拒绝并说明该找谁。
ALTER TABLE ontology_action ADD COLUMN IF NOT EXISTS allowed_experts text;
